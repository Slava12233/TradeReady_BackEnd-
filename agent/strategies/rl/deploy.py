def _configure_logging(verbose: bool) -> None:
    """Configure structlog for the deploy CLI.

    Args:
        verbose: When ``True``, enable DEBUG-level output.
    """
    from agent.logging import configure_agent_logging  # noqa: PLC0415

    configure_agent_logging(log_level="DEBUG" if verbose else "INFO")


# ── CLI ────────────────────────────────────────────────────────────────────────


def _build_parser() -> argparse.ArgumentParser:
    """Build the CLI argument parser.

    Returns:
        Configured :class:`argparse.ArgumentParser`.
    """
    p = argparse.ArgumentParser(
        prog="python -m agent.strategies.rl.deploy",
        description=(
            "Deploy a trained PPO model against the TradeReady platform.  "
            "Use --mode backtest (safe) or --mode live (paper-trading sandbox)."
        ),
        formatter_class=argparse.ArgumentDefaultsHelpFormatter,
    )
    p.add_argument(
        "--model",
        type=str,
        default=None,
        metavar="PATH",
        help="Path to the .zip model file (e.g. agent/strategies/rl/models/ppo_seed42.zip).",
    )
    p.add_argument(
        "--mode",
        choices=["backtest", "live"],
        default="backtest",
        help="Execution mode: 'backtest' steps an existing session; 'live' uses real-time data.",
    )
    p.add_argument(
        "--session-id",
        type=str,
        default=None,
        metavar="UUID",
        help="(backtest mode) Existing backtest session UUID to step through.",
    )
    p.add_argument(
        "--steps",
        type=int,
        default=100,
        metavar="N",
        help="Number of decision steps to execute.",
    )
    p.add_argument(
        "--step-interval",
        type=float,
        default=0.0,
        metavar="SEC",
        help="(live mode) Seconds to wait between steps (e.g. 3600 for hourly candles).",
    )
    p.add_argument(
        "--min-order-value",
        type=float,
        default=1.0,
        metavar="USDT",
        help="Minimum order value in USDT.  Orders below this are skipped.",
    )
    p.add_argument(
        "--base-url",
        type=str,
        default=None,
        metavar="URL",
        help="Platform REST API base URL.",
    )
    p.add_argument(
        "--output-dir",
        type=Path,
        default=None,
        metavar="DIR",
        help="Directory to write the deploy log JSON.",
    )
    p.add_argument(
        "--verbose",
        action="store_true",
        help="Enable DEBUG-level log output.",
    )
    return p


def main() -> None:
    """CLI entry point.

    Loads :class:`RLConfig`, applies CLI overrides, constructs a
    :class:`PPODeployBridge`, runs the requested mode, saves the deploy log,
    and prints a summary.
    """
    from agent.strategies.rl.config import RLConfig  # noqa: PLC0415

    p = _build_parser()
    args = p.parse_args()
    _configure_logging(args.verbose)

    try:
        config = RLConfig()
    except Exception as exc:
        log.error("agent.strategy.rl.deploy.config.load_failed", error=str(exc))
        sys.exit(1)

    overrides: dict[str, Any] = {}
    if args.base_url is not None:
        overrides["platform_base_url"] = args.base_url
    if overrides:
        config = config.model_copy(update=overrides)

    if not config.platform_api_key:
        log.error(
            "agent.strategy.rl.deploy.config.missing_api_key",
            hint="Set RL_PLATFORM_API_KEY in agent/.env or as environment variable",
        )
        sys.exit(1)

    # Resolve model path.
    model_path: str | None = args.model
    if model_path is None:
        # Auto-discover: pick ppo_seed42.zip if it exists, else first available.
        candidate = config.models_dir / "ppo_seed42.zip"
        if candidate.exists():
            model_path = str(candidate)
        else:
            found = sorted(config.models_dir.glob("ppo_seed*.zip"))
            if found:
                model_path = str(found[0])

    if model_path is None or not Path(model_path).exists():
        log.error(
            "agent.strategy.rl.deploy.model_not_found",
            path=model_path,
            hint="Train a model first with: python -m agent.strategies.rl.runner",
        )
        sys.exit(1)

    output_dir: Path = (
        args.output_dir
        if args.output_dir is not None
        else Path(__file__).parent.parent.parent / "reports"
    )

    bridge = PPODeployBridge(
        model_path=model_path,
        config=config,
        mode=args.mode,
        min_order_value=Decimal(str(args.min_order_value)),
    )

    try:
        if args.mode == "backtest":
            if args.session_id is None:
                log.error(
                    "agent.strategy.rl.deploy.missing_session_id",
                    hint="Provide --session-id <uuid> for backtest mode.",
                )
                sys.exit(1)
            deploy_log = asyncio.run(
                bridge.run_backtest(session_id=args.session_id, n_steps=args.steps)
            )
        else:
            deploy_log = asyncio.run(
                bridge.run_live(
                    n_steps=args.steps,
                    step_interval_sec=args.step_interval,
                )
            )
    except KeyboardInterrupt:
        log.info("agent.strategy.rl.deploy.interrupted")
        sys.exit(0)
    except Exception as exc:
        log.exception("agent.strategy.rl.deploy.failed", error=str(exc))
        sys.exit(1)

    saved_path = _save_deploy_log(deploy_log, output_dir)

    # Log deploy summary.
    log.info(
        "agent.strategy.rl.deploy.summary",
        mode=deploy_log.mode,
        session_id=deploy_log.session_id,
        model_path=deploy_log.model_path,
        total_steps=deploy_log.total_steps,
        orders_placed=deploy_log.total_orders_placed,
        orders_skipped=deploy_log.total_orders_skipped,
        start_equity=str(deploy_log.start_equity),
        final_equity=str(deploy_log.final_equity),
        wall_time_sec=round(deploy_log.wall_time_sec, 1),
        log_file=str(saved_path),
    )


if __name__ == "__main__":
    main()
